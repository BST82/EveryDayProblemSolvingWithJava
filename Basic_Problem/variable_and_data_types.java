package Basic_Problem;

public class variable_and_data_types {
    public static void main(String args[]) {
        // primitive data types (already exist in java)
        // total 8 datatypes

        byte x = 78;
        int a = 1067890999; // integer type variables
        String b = "My name"; // string
        short c = 32345; // short
        boolean flag = true;
        char d = 't'; // character inclosed with singlequotes
        long e = 778901234; // long
        float f = 3.6f; // float
        double dd = 66.78;

        System.out.println(x);
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(flag);
        System.out.println(d);
        System.out.println(f);
        System.out.println(dd);
    }
}
