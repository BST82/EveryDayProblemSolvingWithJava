package Operators;

public class relation_operator {
    public static void main(String[] args) {
        /*
         * relation operator
         * 1) == (equal to) a==b
         * 2) != (Not equal to) a!=b;
         * 3) > ( greater ) a>b;
         * 4) < (less)a<b;
         * 5) >= ( greater or equal ) a>b;
         * 6) <= (less or equal )a<b;
         */

        int A = 10;
        int B = 5;
        System.out.println(A == B);
        System.out.println(A != B);
        System.out.println(A > B);
        System.out.println(A < B);
        System.out.println(A >= B);
        System.out.println(A <= B);

    }
}
